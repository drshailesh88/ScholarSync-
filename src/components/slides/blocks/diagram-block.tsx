"use client";

import { useEffect, useRef, useId } from "react";
import type { DiagramData, ThemeConfig } from "@/types/presentation";
import mermaid from "mermaid";

mermaid.initialize({ startOnLoad: false, theme: "default" });

// ---------------------------------------------------------------------------
// Mermaid render queue — serializes all renders to prevent race conditions.
// Mermaid.js uses internal DOM state that corrupts when multiple render() calls
// run concurrently, producing empty <g></g> SVGs.
// ---------------------------------------------------------------------------
let renderQueue: Promise<void> = Promise.resolve();

function queueMermaidRender(
  id: string,
  syntax: string,
): Promise<string> {
  return new Promise((resolve, reject) => {
    renderQueue = renderQueue.then(async () => {
      try {
        // Clean up any leftover temp element from a prior render with this id
        document.querySelector(`#d${id}`)?.remove();
        const { svg } = await mermaid.render(id, syntax);
        resolve(svg);
      } catch (err) {
        reject(err);
      }
    });
  });
}

function isSvgEmpty(svg: string): boolean {
  // Mermaid produces <style>...</style><g></g> when render fails silently
  return !svg.includes("<path") && !svg.includes("<rect") && !svg.includes("<text") && !svg.includes("<polygon");
}

function fixSvgScaling(container: HTMLDivElement) {
  const svgEl = container.querySelector("svg");
  if (!svgEl) return;
  if (!svgEl.getAttribute("viewBox")) {
    // Mermaid sometimes omits viewBox, causing 300x150 default size
    const bbox = svgEl.getBBox();
    if (bbox.width > 0 && bbox.height > 0) {
      const pad = 8;
      svgEl.setAttribute(
        "viewBox",
        `${bbox.x - pad} ${bbox.y - pad} ${bbox.width + pad * 2} ${bbox.height + pad * 2}`,
      );
    }
  }
  svgEl.setAttribute("width", "100%");
  svgEl.removeAttribute("height");
  svgEl.style.maxHeight = "100%";
}

interface DiagramBlockProps {
  data: DiagramData;
  theme: ThemeConfig;
}

export function DiagramBlock({ data, theme }: DiagramBlockProps) {
  const ref = useRef<HTMLDivElement>(null);
  const uid = useId().replace(/:/g, "_");

  useEffect(() => {
    if (!ref.current || !data.syntax) return;
    let cancelled = false;

    async function renderWithRetry(id: string, attempt: number) {
      try {
        const svg = await queueMermaidRender(id, data.syntax);
        if (cancelled || !ref.current) return;

        if (isSvgEmpty(svg) && attempt < 3) {
          // Empty render — retry after a short delay
          const retryId = `${id}_r${attempt + 1}`;
          setTimeout(() => renderWithRetry(retryId, attempt + 1), 150 * (attempt + 1));
          return;
        }

        // Mermaid SVG output is trusted (generated by the library, not user HTML)
        ref.current.replaceChildren();
        ref.current.insertAdjacentHTML("afterbegin", svg);
        fixSvgScaling(ref.current);
      } catch {
        if (!cancelled && ref.current) {
          const errDiv = document.createElement("div");
          errDiv.style.cssText = "color: #EF4444; font-size: 0.7em;";
          errDiv.textContent = "Diagram syntax error";
          ref.current.replaceChildren(errDiv);
        }
      }
    }

    renderWithRetry(`mermaid_${uid}`, 0);
    return () => { cancelled = true; };
  }, [data.syntax, uid]);

  return (
    <div className="flex flex-col items-center gap-[0.2em] w-full">
      <div
        ref={ref}
        className="w-full flex items-center justify-center [&_svg]:max-w-full [&_svg]:max-h-full"
      />
      {data.caption && (
        <div className="text-[0.55em] opacity-60 italic" style={{ color: theme.textColor }}>
          {data.caption}
        </div>
      )}
    </div>
  );
}
